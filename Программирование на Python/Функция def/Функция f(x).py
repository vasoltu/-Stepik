def f(x):                # 'Создание функции'
    if x <= -2:          # '1 условие(1 формула)'
        x = 1 - (x + 2) ** 2
        return x
    elif -2 < x <= 2:    # '2 условие(2 формула)'
        x = -(x / 2)
        return x
    elif 2 < x:          # '3 условие(3 формула)'
        x = 1 + (x - 2) ** 2
        return x


x = 0
f(x)                     # 'Вызов функции'
